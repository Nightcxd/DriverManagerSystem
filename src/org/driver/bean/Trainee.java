package org.driver.bean;

/*
 * 驾校学员
 */
public class Trainee {
	private String t_id;//id
	private String t_name;//姓名
	private String t_sex;//性别
	private String t_tel;//电话
	private String t_idcard;//身份证号码
	private String t_icon;//照片
	private String t_signTime;//报名时间
	private String t_schooling;//学费金额
	private String t_trainStatus;//考试进度
	public String getT_id() {
		return t_id;
	}
	public void setT_id(String t_id) {
		this.t_id = t_id;
	}
	public String getT_name() {
		return t_name;
	}
	public void setT_name(String t_name) {
		this.t_name = t_name;
	}
	public String getT_sex() {
		return t_sex;
	}
	public void setT_sex(String t_sex) {
		this.t_sex = t_sex;
	}
	public String getT_tel() {
		return t_tel;
	}
	public void setT_tel(String t_tel) {
		this.t_tel = t_tel;
	}
	public String getT_idcard() {
		return t_idcard;
	}
	public void setT_idcard(String t_idcard) {
		this.t_idcard = t_idcard;
	}
	public String getT_icon() {
		return t_icon;
	}
	public void setT_icon(String t_icon) {
		this.t_icon = t_icon;
	}
	public String getT_signTime() {
		return t_signTime;
	}
	public void setT_signTime(String t_signTime) {
		this.t_signTime = t_signTime;
	}
	public String getT_schooling() {
		return t_schooling;
	}
	public void setT_schooling(String t_schooling) {
		this.t_schooling = t_schooling;
	}
	public String getT_trainStatus() {
		return t_trainStatus;
	}
	public void setT_trainStatus(String t_trainStatus) {
		this.t_trainStatus = t_trainStatus;
	}
	
}

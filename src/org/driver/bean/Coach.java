package org.driver.bean;

/*
 * 驾校教练
 */
public class Coach {
	private String c_id;//教练id
	private String c_name;//教练名字
	private String c_tel;//教练电话
	private String c_icon;//教练头像
	public String getC_id() {
		return c_id;
	}
	public void setC_id(String c_id) {
		this.c_id = c_id;
	}
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_tel() {
		return c_tel;
	}
	public void setC_tel(String c_tel) {
		this.c_tel = c_tel;
	}
	public String getC_icon() {
		return c_icon;
	}
	public void setC_icon(String c_icon) {
		this.c_icon = c_icon;
	}
	
}
